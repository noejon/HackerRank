function validateIPv4(address) {
    return /^(?:(?:2(?:[0-4][0-9]|5[0-5])|1[0-9][0-9]|[1-9][0-9]|0{1,2}[0-7]|[0-9])\.){3}(?:(?:2(?:[0-4][0-9]|5[0-5])|1[0-9][0-9]|[1-9][0-9]|0{1,2}[0-7]|[0-9]))$/.test(address);
}

function validateIPv6(address) {
    return /^((([0-9a-f]{1,4}:){7}[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){6}:[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){5}:([0-9a-f]{1,4}:)?[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){4}:([0-9a-f]{1,4}:){0,2}[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){3}:([0-9a-f]{1,4}:){0,3}[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){2}:([0-9a-f]{1,4}:){0,4}[0-9a-f]{1,4})|(([0-9a-f]{1,4}:){6}((\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b)\.){3}(\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b))|(([0-9a-f]{1,4}:){0,5}:((\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b)\.){3}(\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b))|(::([0-9a-f]{1,4}:){0,5}((\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b)\.){3}(\b((25[0-5])|(1[0-9]{2})|(2[0-4][0-9])|(0{1,2}[0-7])|[0-9])\b))|([0-9a-f]{1,4}::([0-9a-f]{1,4}:){0,5}[0-9a-f]{1,4})|(::([0-9a-f]{1,4}:){0,6}[0-9a-f]{1,4})|::|(([0-9a-f]{1,4}:){1,7}:))$/.test(address);
}

function getAddressType(address) {
    if (validateIPv6(address)) {
        return "IPv6";
    }
    if (validateIPv4(address)) {
        return "IPv4";
    }
    return "Neither";
}

function validateAddresses(addresses) {
    return addresses.map(address => getAddressType(address));
}